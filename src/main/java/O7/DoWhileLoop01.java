package O7;

public class DoWhileLoop01 {
    public static void main(String[] args) {
        int i=1;
        while (i<1){
            System.out.println("While Loop");
            i++;
        }
        int k=1;
        do {
            System.out.println("Do while loop");
            k++;
        }while (k<1);
//do-while loop oyun programi yazmak icin iyidir
    }
}
