package A11;

public class ForLoop07 {
    public static void main(String[] args) {

        int satir=3;
        int sutun=5;
        for (int i=1;i<=satir;i++){
            for (int j=1;j<=sutun;j++){
                System.out.print("X");

            }
            System.out.println();
        }

    }
}
