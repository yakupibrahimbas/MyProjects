package A11;

public class DoWhileLoop01 {
    public static void main(String[] args) {
        //baslangic degeri
        //while (loop calisma kurali)
        //calisacak kodlar
        //artirma/eksiltme
        //do while dongusu en az bir kez caliscagindan emin olmak istedihiniz zmaan kullanilir.

        //5 dahil 3 e kadar olan tam sayilari console a yazdiriniz

        int i=5;
        do {
            System.out.println(i);
            i--;
        }while (i>2);



    }
}
