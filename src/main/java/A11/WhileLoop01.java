package A11;

public class WhileLoop01 {
    public static void main(String[] args) {

        int i=3;
        while (i<7){
            System.out.println(i);

            i++;
        }
        System.out.println("--------------");

        int k=23;
        while (k>11){
            if (k%2==0){
                System.out.println();
                System.out.println(k);
            }
            k--;
        }


    }
}
