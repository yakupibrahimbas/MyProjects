package A7;

public class WhileLoops01 {
    public static void main(String[] args) {

        //Example: Type code to print integers from 4 to 7

        //1.way

        for (int i=4;i<8;i++){
            System.out.println(i+" ");
        }

        //2.way

        int i=4;
        while (i<8){
            System.out.println(i+" ");
            i++;
        }

    }
}
