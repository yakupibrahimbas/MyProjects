package O12;

public class While {
    public static void main(String[] args) {

        //While
        for (int i = 1; i <100 ; i++) {
            System.out.println(i);
        }
        System.out.println("For dongusu bitti");

        int i=1;
        while (i<100){

            System.out.println(i);
            i+=2;
        }
        System.out.println("While dongusu bitti");
    }
}
