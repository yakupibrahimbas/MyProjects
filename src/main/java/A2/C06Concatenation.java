package A2;

public class C06Concatenation {
    public static void main(String[] args) {

        //Caoncatination is an operation to join Strings with Strings or to join String with other data types
        //symbol of concatenation is "+"

        System.out.println("Tom"+"Hanks");
        System.out.println("Tom "+"Hanks");
        System.out.println("Tom"+" Hanks");
        System.out.println("Tom"+" " + "Hanks");
        System.out.println("Tom"+3+2); //Tom32
        System.out.println("Tom"+(3+2)); //Tom5
        System.out.println("Tom"+3*2);//Tom6
        System.out.println(3+2+"Tom");//5Tom
        System.out.println(3*2+4+"Tom"+5+6);//10Tom


    }
}
