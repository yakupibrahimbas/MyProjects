package A12;

public class Loop02 {
    public static void main(String[] args) {

        for (int i=3;i<8;i++){
            System.out.println(i);
        }
        System.out.println("-----------");
        int j=3;
        while (j<8){
            System.out.println(j);
            j++;
        }
    }
}
