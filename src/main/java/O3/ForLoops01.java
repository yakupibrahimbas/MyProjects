package O3;

public class ForLoops01 {
    public static void main(String[] args) {

        //ekrana 10 kere hi yazin

        for (int i = 0; i < 11; i++) {
            System.out.println("hi");
        }
        for (int i = 100; i >0 ; i--) { //100 den 1 e kadar olan syilari yazin
            System.out.print(i+" ");
        }
    }
}
